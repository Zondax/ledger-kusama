#define SWAP_BYTES(x, y, tmp) { \
                   tmp = x;     \
                   x = y;       \
                   y = tmp;\
}

#define SWAP_ENDIAN(x, tmp) { \
                 for (int i = 0; i < ED25519_SCALAR_BYTES/2; i++){ \
                          SWAP_BYTES(*(x + i), *(x + (ED25519_SCALAR_BYTES-1-i)), tmp); \
                 }          \
}

